class Downsample(nn.Module):
    def __init__(self, n_feat):
        super(Downsample, self).__init__()
        self.body = nn.Sequential(nn.Conv2d(n_feat, n_feat // 8, kernel_size=3, stride=1, padding=1, bias=False),
                                  nn.PixelUnshuffle(8))

    def forward(self, x):
        return self.body(x)

class Upsample(nn.Module):
    def __init__(self, n_feat):
        super(Upsample, self).__init__()
        self.body = nn.Sequential(nn.Conv2d(n_feat, n_feat * 8, kernel_size=3, stride=1, padding=1, bias=False),
                                  nn.PixelShuffle(8))

    def forward(self, x):
        return self.body(x)



