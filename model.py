import torch
import torch.nn as nn
import torch.nn.functional as F
import numbers
import math
from einops import rearrange


class TextFuse(nn.Module):
    def __init__(self, model_clip, vi_inc=1, ir_inc=1, out_channels=1,
                 dim=32, num_blocks=[2, 2, 2, 2],
                 num_refinement_blocks=4,
                 heads=[1, 2, 4, 8],
                 ffn_expansion_factor=2,
                 bias=False,
                 LayerNorm_type='WithBias',
                 ):
        super(TextFuse, self).__init__()

        self.model_clip = model_clip
        self.model_clip.eval()

        self.encoder_vi = Encoder_vi(inp_channels=vi_inc, dim=dim, num_blocks=num_blocks, heads=heads,
                                     ffn_expansion_factor=ffn_expansion_factor, bias=bias,
                                     LayerNorm_type=LayerNorm_type)
        self.encoder_ir = Encoder_ir(inp_channels=ir_inc, dim=dim, num_blocks=num_blocks, heads=heads,
                                     ffn_expansion_factor=ffn_expansion_factor, bias=bias,
                                     LayerNorm_type=LayerNorm_type)

        self.down = Downsample(dim)
        self.cross_attention_down = Cross_attention(dim * 8)
        self.up = Upsample(dim * 8)
        self.attention_spatial_down = Attention_spatial(dim * 8)

        self.feature_fusion_down = Fusion_Embed(embed_dim=dim * 8)
        self.feature_fusion = Fusion_Embed(embed_dim=dim)

        self.decoder = nn.Sequential(*[
            TransformerBlock(dim=int(dim), num_heads=heads[1], ffn_expansion_factor=ffn_expansion_factor,
                             bias=bias, LayerNorm_type=LayerNorm_type) for i in range(num_blocks[1])])

        self.prompt_guidance = FeatureWiseAffine(in_channels=512, out_channels=dim)

        self.output = nn.Conv2d(int(dim), out_channels, kernel_size=3, stride=1, padding=1, bias=bias)

    def forward(self, vi, ir, vis_text, ir_text,  vis_target_text ):
        b = vi.shape[0]
 
        vis_text_features = self.get_text_feature(vis_text.expand(b, -1)).to(vi.dtype)
        ir_text_features = self.get_text_feature(ir_text.expand(b, -1)).to(vi.dtype)
        vis_target_text_features = self.get_text_feature(vis_target_text.expand(b, -1)).to(vi.dtype)

        vi = self.encoder_vi(vi)
        ir = self.encoder_ir(ir)


        vi_add_text = self.prompt_guidance(vi, vis_text_features)
        ir_add_text = self.prompt_guidance(ir, ir_text_features)
        vi_ir_add_text_fuse = self.feature_fusion(vi_add_text, ir_add_text)


        vi_down = self.down(vi)
        ir_down = self.down(ir)
        vi_down_cross, ir_down_cross = self.cross_attention_down(vi_down, ir_down)
        down_fuse = self.feature_fusion_down(vi_down_cross, ir_down_cross)
        out_down_fuse = self.attention_spatial_down(down_fuse)
        out_down_fuse_up = self.up(out_down_fuse)

        encoder_out = self.feature_fusion(vi_ir_add_text_fuse, out_down_fuse_up)

        vi_ir_text = self.prompt_guidance(encoder_out, vis_target_text_features)
        decoder_con = self.decoder(vi_ir_text)

        out = self.output(decoder_con)
        return out

    @torch.no_grad()
    def get_text_feature(self, text):
        text_feature = self.model_clip.encode_text(text)
        return text_feature

class Cross_attention(nn.Module):
    def __init__(self, in_channel, n_head=1, norm_groups=16):
        super().__init__()
        self.n_head = n_head
        self.norm_A = nn.GroupNorm(norm_groups, in_channel)
        self.norm_B = nn.GroupNorm(norm_groups, in_channel)
        self.qkv_A = nn.Conv2d(in_channel, in_channel * 3, 1, bias=False)
        self.out_A = nn.Conv2d(in_channel, in_channel, 1)

        self.qkv_B = nn.Conv2d(in_channel, in_channel * 3, 1, bias=False)
        self.out_B = nn.Conv2d(in_channel, in_channel, 1)

    def forward(self, x_A, x_B):
        batch, channel, height, width = x_A.shape

        n_head = self.n_head
        head_dim = channel // n_head

        x_A = self.norm_A(x_A)
        qkv_A = self.qkv_A(x_A).view(batch, n_head, head_dim * 3, height, width)
        query_A, key_A, value_A = qkv_A.chunk(3, dim=2)

        x_B = self.norm_B(x_B)
        qkv_B = self.qkv_B(x_B).view(batch, n_head, head_dim * 3, height, width)
        query_B, key_B, value_B = qkv_B.chunk(3, dim=2)

        attn_A = torch.einsum(
            "bnchw, bncyx -> bnhwyx", query_B, key_A
        ).contiguous() / math.sqrt(channel)
        attn_A = attn_A.view(batch, n_head, height, width, -1)
        attn_A = torch.softmax(attn_A, -1)
        attn_A = attn_A.view(batch, n_head, height, width, height, width)

        out_A = torch.einsum("bnhwyx, bncyx -> bnchw", attn_A, value_A).contiguous()
        out_A = self.out_A(out_A.view(batch, channel, height, width))
        out_A = out_A + x_A

        attn_B = torch.einsum(
            "bnchw, bncyx -> bnhwyx", query_A, key_B
        ).contiguous() / math.sqrt(channel)
        attn_B = attn_B.view(batch, n_head, height, width, -1)
        attn_B = torch.softmax(attn_B, -1)
        attn_B = attn_B.view(batch, n_head, height, width, height, width)

        out_B = torch.einsum("bnhwyx, bncyx -> bnchw", attn_B, value_B).contiguous()
        out_B = self.out_B(out_B.view(batch, channel, height, width))
        out_B = out_B + x_B

        return out_A, out_B

class Attention_spatial(nn.Module):
    def __init__(self, in_channel, n_head=1, norm_groups=16):
        super().__init__()

        self.n_head = n_head

        self.norm = nn.GroupNorm(norm_groups, in_channel)
        self.qkv = nn.Conv2d(in_channel, in_channel * 3, 1, bias=False)
        self.out = nn.Conv2d(in_channel, in_channel, 1)

    def forward(self, input):
        batch, channel, height, width = input.shape
        n_head = self.n_head
        head_dim = channel // n_head
        norm = self.norm(input)
        qkv = self.qkv(norm).view(batch, n_head, head_dim * 3, height, width)
        query, key, value = qkv.chunk(3, dim=2)
        attn = torch.einsum(
            "bnchw, bncyx -> bnhwyx", query, key
        ).contiguous() / math.sqrt(channel)
        attn = attn.view(batch, n_head, height, width, -1)
        attn = torch.softmax(attn, -1)
        attn = attn.view(batch, n_head, height, width, height, width)

        out = torch.einsum("bnhwyx, bncyx -> bnchw", attn, value).contiguous()
        out = self.out(out.view(batch, channel, height, width))

        return out + input

